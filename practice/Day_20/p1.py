# Errors In python

# 1. IndexError   (when we try to access an index which is out of range)
# 2. KeyError     (when we try to access a key which does not exist in a dictionary)
# 3. TypeError    (when we try to perform an operation on an object of an inappropriate type)
# 4. ModuleNotFoundError (when we try to import a module that does not exist)
# 5. ValueError   (when we try to perform an operation on a value that is inappropriate for the type)
# 6. NameError    (when an object is not defined)
# 7. AttributeError (when we try to access an attribute that does not exist in an object)

