num1 = int(input("enter first number:"))
num2 = int(input('enter second number:'))

add = num1 + num2
sub = num1 - num2
mul = num1 * num2
div = num1 / num2
mod = num1 % num2
exp = num1 ** num2
flD = num1 // num2

print("the sum of", num1, "and", num2, "is:", add)
print("the difference of", num1, "and", num2, "is:", sub)
print("the product of", num1, "and", num2, "is:", mul)
print("the quotient of", num1, "and", num2, "is:", div)
print("the remainder of", num1, "and", num2, "is:", mod)
print("the exponent of", num1, "and", num2, "is:", exp)
print("the floor division of", num1, "and", num2, "is:", flD)
